const mongoose = require('mongoose');
const { Schema } = mongoose;

// Define the schema for Item
const ItemSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

// Create a model for Item
const Item = mongoose.model('Item', ItemSchema);

module.exports = Item;
